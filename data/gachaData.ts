// Keywords to auto-determine high rarity
const LEGENDARY_KEYS = ['dragon', 'djinn', 'hydra', 'kraken', 'behemoth', 'abyssal', 'reaper', 'griffin', 'tengu', 'wraith', 'minotaur', 'cyclops', 'gorgon', 'basilisk'];
const EPIC_KEYS = ['cyber', 'golem', 'spirit', 'mage', 'knight', 'robot', 'beast', 'automaton', 'thunder', 'magma', 'holy', 'dark', 'banshee'];
const RARE_KEYS = ['slime', 'warrior', 'turret', 'fighter', 'imp', 'wisp', 'monster', 'bear', 'wolf', 'tiger', 'shark', 'cobra', 'viper', 'mantis', 'scorpion', 'mammoth'];

// Determine rarity based on name
const getRarity = (name: string): Rarity => {
    const lower = name.toLowerCase();
    if (LEGENDARY_KEYS.some(k => lower.includes(k))) return 'LEGENDARY';
    if (EPIC_KEYS.some(k => lower.includes(k))) return 'EPIC';
    if (RARE_KEYS.some(k => lower.includes(k))) return 'RARE';
    // Random chance for some "normal" items to be rare
    const hash = name.split('').reduce((a,b)=>a+b.charCodeAt(0),0);
    if (hash % 10 === 0) return 'RARE'; 
    return 'COMMON';
};

export const GACHA_ITEMS: GachaItem[] = rawData.split('\n').map((line, idx) => {
    const [rawName, imageId] = line.split('|').map(s => s.trim());
    const name = rawName.replace('.png', '');
    return {
        id: `gacha_${idx}`,
        name: name.split(' ').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' '), // Capitalize
        imageId: imageId,
        rarity: getRarity(name)
    };
});
